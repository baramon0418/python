#파이썬 문법 중 == 연산자를 이용한 출력 결과를 설명하시오.
#아래 코드의 결과로 출력되는 값은 무엇인지 설명하시오(True,False 둘중 하나로 출력됨)

#[1]
a = [1,2,3,4,5]
b = a
c = [1,2,3,4,5]

#[2]: 다음의 출력 결과를 말해보시오
print('[2-1] a == b = ',a == b)
print('[2-1] a == c = ',a == c)
print('[2-1] b == c = ',b == c)

