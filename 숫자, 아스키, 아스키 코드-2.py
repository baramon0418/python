#[1]: 아스키 코드란?
#컴퓨터 내부에서 숫자 또는 문자에 대한 정보처리를 이진수로 처리한다. 0과 1 두개로만 처리 --> 즉, 문자도 숫자로 기억한다.
#이때, 문자와 숫자를 대응시켜줘야 하는데 여러 방법 중 아스키 코드 방식을 사용한다.
#영어 알파벳 각각의 문자에 번호를 붙여서 처리 --> 번호를 붙일 때 아스키 규약에 따라 붙임 --> 아스키 코드


